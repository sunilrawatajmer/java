import java.util.*;
class betwise{
    public static void main(String arr[]) {
         int a;

         a=4&5;
         System.out.println(a);

         a=4|5;
         System.out.println(a);

         a=4^5;
         System.out.println(a);

         a=8>>1;
         System.out.println(a);

         a=8<<1;
         System.out.println(a);
    }
}